CREATE TABLE temp_load_table AS
SELECT o.id   offence_id,
       oi.offence_code,
       oi.indicator_code,
       s.code schedule_code,
       sp.part_number,
       sp.id  schedule_part_id
FROM   offence_indicator oi
           JOIN offence o ON oi.offence_code = o.code
           JOIN schedule s
                ON s.code = CASE
                                WHEN oi.indicator_code = 'SCH13' THEN '13'
                                WHEN oi.indicator_code = 'S15/CJIB' THEN '15'
                                WHEN oi.indicator_code = 'SCH15/CJIB/L' THEN '15-LIFE'
                                WHEN oi.indicator_code = 'SCH17A' THEN '17A'
                                WHEN oi.indicator_code = 'SCH17A2' THEN '17A'
                                WHEN oi.indicator_code = 'SCH19ZA' THEN '19ZA'
                                WHEN oi.indicator_code = 'PCSC/SDS' THEN 'PCSC'
                                WHEN oi.indicator_code = 'PCSC/SDS+' THEN 'PCSC'
                                WHEN oi.indicator_code = 'PCSC/SEC250' THEN 'PCSC'
                    END
           JOIN schedule_part sp ON sp.schedule_id = s.id
WHERE  ( oi.indicator_code IN ( 'SCH17A2', 'PCSC/SDS+' ) AND sp.part_number = 2 )
   OR ( oi.indicator_code = 'PCSC/SEC250'  AND sp.part_number = 3 )
   OR ( oi.indicator_code NOT IN ( 'SCH17A2', 'PCSC/SDS+', 'PCSC/SEC250' ) AND sp.part_number = 1 );

INSERT INTO offence_schedule_mapping
(schedule_part_id,
 offence_id)
SELECT DISTINCT schedule_part_id,
                offence_id
FROM   temp_load_table tlt
WHERE  NOT EXISTS (SELECT NULL
                   FROM   offence_schedule_mapping osm
                   WHERE  osm.offence_id = tlt.offence_id
                     AND osm.schedule_part_id = tlt.schedule_part_id);

